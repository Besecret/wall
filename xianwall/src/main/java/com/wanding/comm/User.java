package com.wanding.comm;

public enum User {

    student("1", "学生"), local_resident("2", "西安市本地市民"), Long_term_resident("3", "西安市常住人口"),other("4","管理员");

    private String id;
    private String name;


    private User(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }



    public String getName() {
        return name;
    }


    public void setName(String name) {
        this.name = name;
    }


}
